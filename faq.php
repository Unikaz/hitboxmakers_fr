    <h1 class="center-content">FAQ Game Jam 2023</h1>

    <h2>Où et Quand ?</h2>
    <p>
        Cette édition aura lieu à l'école Brassart, au 1 rue Léo-Delibes. On vous attend à partir de 19h le vendredi
        soir !
    </p>

    <h2>Le matériel mis à disposition</h2>
    <ul>
        <li>Des tables, chaises, électricité, Wifi… Par contre, <strong>emmenez votre matériel !!!</strong></li>
        <li>Un <strong>ministudio</strong> pour vous accompagner dans l'enregistrement de sons pour vos jeux</li>
        <li>Une imprimante couleur A3 (Nous pourrons faire assez de copies, mais le nombre ne sera pas illimité)</li>
        <li>Des planches à découper, cutters, règles. Mais emmenez votre papier/carton</li>
        <li>De la lumière pour faire de jolies photos</li>
        <li>Du café !</li>
    </ul>

    <h2>Planning</h2>
    <div>
        <h3>Vendredi à partir de 19h</h3>
        <p>
            On vous accueille et on commence par une soirée animée où nous voterons tous ensemble les thèmes pour la
            Jam, le tout accompagné par des pizzas.
        </p>
        <p>
            Ce sera aussi l'occasion d'échanger avec les autres participants et de <strong>créer votre équipe !</strong>
        </p>

        <h3>Samedi</h3>
        <p>Samedi vous pourrez vous donner à fond dans le développement de votre jeu !</p>
        <p>Nous passerons vous voir pour que vous puissiez nous présenter votre <strong>proto vers 18h</strong>.</p>
