    <h2>Les 10 (ou presque) Commandements de la Game Jam</h2>
    En 8 Games Jam organisées, ça fait beaucoup de jeux fantastiques ! Mais aussi quelques écueils à éviter...
    Alors, rien que pour vous, voici notre sagesse que nous vous distillons en 10 commandements :

    <div id="ten-commandments">
      <strong>A 18h le samedi, ton prototype tu auras.</strong><br>
      Ou un mur tu te prendras…
      <hr>

      <strong>D'excès de confiance, tu ne pêcheras point.</strong><br>
      Ne supposez pas de vos capacités, ou vous vous planterez !
      <hr>

      <strong>Si tu n’es pas nocturne, reste avec une équipe diurne.</strong><br>
      Il est important que l’équipe puisse travailler ensemble, et non en décalé.
      <hr>

      <strong>La nuit porte conseil.</strong><br>
      L’expérience montre qu’il est bien de se reposer pour mieux atteindre ses attentes. Ne vous précipitez pas !
      <hr>

      <strong>L’union ne fait pas la vitesse.</strong><br>
      Plus on est nombreux, plus on est lent. Trois ou quatre personnes, c’est un bon nombre.
      <hr>

      <strong>Connais tes armes.</strong><br>
      Préfères les outils que tu maîtrises, ou des outils très simple d’usage.
      <hr>

      <strong>Après l’heure ce n’est plus l’heure.</strong><br>
      Il faut un jeu à la fin du week-end. Ne visez pas comme objectif un jeu qui finirez après la Jam. C’est le meilleur moyen de se planter et d’être frustré.
      <hr>

      <strong>Allié d’un jour.</strong><br>
      Vous vous engagez les uns aux autres pour le temps du week-end. Après chacun retrouve sa liberté. Aucune obligation de continuer à travailler sur le jeu !
      <hr>

      <strong>Les bons comptes font les bons amis.</strong><br>
      Si quelqu'un veut continuer, voire le vendre, informez-en au moins les autres (pendant ou après le Jam)
      <hr>

      <strong>Pour travailler en équipe</strong><br>
      Vierge d'idées tu viendras

