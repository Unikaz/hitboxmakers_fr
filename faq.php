        <h2>Les 10 (ou presque) Commandements de la Game Jam</h2>
        <p>En 10 Games Jam que nous avons organisés, ça fait beaucoup de jeux fantastiques ! Mais aussi quelques écueils
            à éviter... Alors, rien que pour vous, voici notre sagesse que nous vous distillons en 10 commandements :
            (vous pouvez aussi télécharger la
            <a href="https://drive.google.com/file/d/1x4ypyT9E999j5n413_zgHHnmxTrH6Meg/view?usp=sharing">version pdf</a>,
            plus jolie)
        </p>

        <div id="ten-commandments">

            <strong>Samedi à 18h, ton prototype tu auras,</strong><br>
            Ou un mur tu te prendras… Pour trouver une équipe, ton esprit tu videras
            <hr>
            <strong>Pour trouver une équipe, ton esprit tu videras</strong><br>
            Concevez vos idées en équipe, plutôt que d’imposer la vôtre.
            <hr>
            <strong>Soyez simple ! Soyez direct ! Soyez imparfait !</strong><br>
            Car tout ce que vous ferez prendra plus de temps qu’attendu...
            <hr>
            <strong>Ce n’est pas fini ? Ce n’est pas grave : publie !</strong><br>
            Quoi que vous ayez réussi à faire, vous devriez publier quelque chose. <br>
            Pour vous, pour votre équipe.
            <hr>
            <strong>La taille compte. Le format aussi.</strong><br>
            Graphismes, sons… Définissez les formats, et mettez des placeholders. Pour l’intégration, ça vous aidera.
            <hr>
            <strong>Connais tes armes ! Ne te tire pas dans le pied...</strong><br>
            Préférez des outils simples ou que vous maîtrisez. Sinon, tout prendra plus de temps.
            <hr>
            <strong>L’union ne fait pas la vitesse.</strong><br>
            Plus on est nombreux, plus on est lent ! Une équipe à trois ou quatre, c’est bien.
            <hr>
            <strong>Si tu n’es pas nocturne, choisis une équipe diurne.</strong><br>
            C’est mieux de travailler ensemble, et non en décalé. Sinon, vous risquez de vous disputer.
            <hr>
            <strong>Se reposer, c’est autorisé</strong><br>
            Vous n’êtes pas obligés de rusher ! <br>Les meilleurs jeux viennent souvent de ceux qui se sont le mieux
            reposés.
            <hr>
            <strong>Alliés d’un jour, amis toujours.</strong><br>
            Engagez-vous les uns les autres pour le temps du week-end. Ensuite, retrouvez votre liberté. <br>
            C’est un bon moyen pour tout donner !
            <hr>


            <strong>A 18h le samedi, ton prototype tu auras.</strong><br>

