  <h2>Planning</h2>
  <p>
    La jam commencera lorsque le thème sera révélé, <strong>vendredi 28/01</strong> probablement à <strong>21h</strong>.
    Et elle finira <strong>dimanche 30/01 à 17h</strong>.
  </p>
  <p>
    Vous aurez alors un peu de temps pour mettre le jeu en ligne, et nous passerons le reste de la soirée pour souffler un peu et pour tester nos jeux ensemble !
  </p>
  <h3>Vendredi 28</h3>
  <strong>19h00 - 20h00</strong> Accueil - jeux brise-glace
  <p>
    C’est le moment idéal pour faire connaissance ! Des canaux seront ouverts pour discuter, vous présentez si ce n’est pas fait, il y aura des petits jeux marrants (scribble.io, Gartic Phone, cover pitch game) pour briser la glace et faire des rencontres...
  </p>
  <p>
    A votre arrivée, un lien vous sera donné, pour proposer des thèmes locaux (qui viendront enrichir le thème mondial de la Global Game Jam).
  </p>
  <strong>20h00 - 20h30</strong> Vote du thème local !
  <p>En général, c’est le bordel. Mais c’est marrant</p>
  <strong>20h30 - 21h00</strong> Lancement officiel :
  <p>Rappel des conseils pour l’organisation de Jam, keynote officielle Globale Game Jam et révélation du thème mondial !! (Ne trichez pas ;-) et si le connaissez déjà, gardez le secret)</p>
  <strong>21h00</strong> Go go go go !
  <h3>Samedi 29/01</h3>
  <p>
    <strong>10h00 - 12h00</strong> Atelier GDevelop (débutants) Pour ceux qui le souhaite, un atelier sera organisé, Samedi matin entre 10h et midi. Par expérience avec GDevelop même des débutants réussissent à faire des jeux.
  </p>
  <p>
    <strong>12h00</strong> teasing C’est sympa si chaque équipe peut venir écrire quelques lignes, voire poster un croquis sur le canal de discussion
  </p>
  <p>
    <strong>A partir de 18h</strong> présentation des prototypes Pour réussir une Jam, il faut un prototype qui fonctionne le samedi à 18h. Nous proposerons à chaque équipe, si elle le souhaite, de présenter son prototype pendant 10 minutes dans un canal dédié sur le discord (en castant son écran par exemple). Nous établirons un planning dans la journée de Samedi.
  </p>
  <h3>Dimanche 30</h3>
  <p><strong>17h00</strong> C’est la fin !! Mettez les jeux sur <strong><a
          href="https://globalgamejam.org">globalgamejam.org</a></strong> !
    Et j’espère que vous avez mis une petite bière ou un soda au frais pour vous détendre.</p>
  <p>
    <strong>17h30</strong> On commence le Stream Test des jeux ! Sur twitch les organisateurs jouent, tout en discutant avec l’équipe. Soyez attentifs (ou jouez de votre côté), car vous aurez à attribuer des oscars !
  </p>
  <p><strong>19h00</strong> (+ ou -) Soirée des oscars ! Bravo à tous !</p>

